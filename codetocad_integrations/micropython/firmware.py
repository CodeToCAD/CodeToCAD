def _dispatch_tables(mcu: Microcontroller) -> str:
    commands = ", ".join(
        f"{binding.name!r}: command_{_identifier(binding.name)}"
        for binding in mcu.actuators
    )
    sensors = ", ".join(
        f"[{binding.name!r}, read_{_identifier(binding.name)}, "
        f"{int(1000 / (binding.sample_rate_hz or 10))}, 0]"
        for binding in mcu.sensors
    )
    return f"COMMANDS = {{{commands}}}\nSENSORS = [{sensors}]\n"


