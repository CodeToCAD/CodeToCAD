def _mp_camera(binding) -> str:
    """An ESP32-CAM (or any board running a camera-enabled MicroPython
    build with the ``camera`` module). Telemetry is
    ``{"frame": <base64 JPEG>}`` — the same shape the simulated camera
    streams (base64 PNG), so the app's ``add_image`` shows either."""
    var = _identifier(binding.name)
    resolution = tuple(getattr(binding.device, "resolution", (320, 240)))
    framesize = binding.params.get(
        "framesize", _CAMERA_FRAMESIZES.get(resolution, "FRAME_QVGA")
    )
    quality = binding.params.get("quality", 12)
    return (
        f"# --- sensor {binding.name!r}: camera (ESP32-CAM) ---\n"
        f"import binascii\n"
        f"import camera\n"
        f"camera.init(0, format=camera.JPEG, framesize=camera.{framesize})\n"
        f"camera.quality({quality})\n"
        f"def read_{var}():\n"
        f"    frame = camera.capture()\n"
        f"    if not frame:\n"
        f"        return None\n"
        f"    return {{'frame': binascii.b2a_base64(frame).decode().strip()}}\n"
    )


def _mp_imu(binding) -> str:
    var = _identifier(binding.name)
    address = binding.address if binding.address is not None else 0x68
    if binding.bus is not None:
        setup = f"_{var}_i2c = {_bus_var(binding.bus)}\n"
    else:  # legacy form: a dedicated bus from sda/scl pins
        setup = (
            f"from machine import I2C\n"
            f"_{var}_i2c = I2C(0, sda=Pin({binding.pins['sda']}), "
            f"scl=Pin({binding.pins['scl']}))\n"
        )
    return (
        f"# --- sensor {binding.name!r}: MPU6050 IMU over I2C ---\n"
        + setup
        + f"_{var}_i2c.writeto_mem({address}, 0x6B, b'\\x00')  # wake\n"
        f"def read_{var}():\n"
        f"    raw = _{var}_i2c.readfrom_mem({address}, 0x3B, 14)\n"
        f"    def s16(i):\n"
        f"        value = (raw[i] << 8) | raw[i + 1]\n"
        f"        return value - 65536 if value > 32767 else value\n"
        f"    return {{\n"
        f"        'accel': [s16(0) / 16384 * 9.80665, s16(2) / 16384 * 9.80665,\n"
        f"                  s16(4) / 16384 * 9.80665],\n"
        f"        'gyro': [s16(8) / 131 * 0.017453, s16(10) / 131 * 0.017453,\n"
        f"                 s16(12) / 131 * 0.017453],\n"
        f"    }}\n"
    )


_VESC_HELPERS = '''\
# --- VESC UART framing (short packets, CRC16-XMODEM) ---
def _vesc_crc16(payload):
    crc = 0
    for byte in payload:
        crc ^= byte << 8
        for _ in range(8):
            if crc & 0x8000:
                crc = ((crc << 1) ^ 0x1021) & 0xFFFF
            else:
                crc = (crc << 1) & 0xFFFF
    return crc

def _vesc_command(command_id, value):
    payload = bytes([command_id]) + (int(value) & 0xFFFFFFFF).to_bytes(4, "big")
    return (bytes([2, len(payload)]) + payload
            + _vesc_crc16(payload).to_bytes(2, "big") + b"\\x03")
'''

_MICROPYTHON_PCA9685_HELPERS = '''\
# --- PCA9685 16-channel PWM driver helpers ---
def _pca9685_init(i2c, address, freq):
    i2c.writeto_mem(address, 0x00, b"\\x10")  # sleep to set prescale
    prescale = int(25000000 / 4096 / freq + 0.5) - 1
    i2c.writeto_mem(address, 0xFE, bytes([prescale]))
    i2c.writeto_mem(address, 0x00, b"\\xa0")  # restart + auto-increment

def _pca9685_set(i2c, address, channel, fraction):
    ticks = int(min(1.0, max(0.0, fraction)) * 4095)
    i2c.writeto_mem(address, 0x06 + 4 * channel,
                    bytes([0, 0, ticks & 0xFF, ticks >> 8]))
'''


def _mp_i2c_register(binding) -> str:
    var = _identifier(binding.name)
    params = binding.params
    if "register" not in params:
        raise ValueError(
            f"i2c_register binding {binding.name!r} needs "
            'params={"register": <reg>, ...} (optional: length, scale, signed)'
        )
    register = params["register"]
    length = params.get("length", 2)
    scale = params.get("scale", 1.0)
    signed = params.get("signed", False)
    lines = [
        f"# --- sensor {binding.name!r}: I2C register read ---",
        f"def read_{var}():",
        f"    raw = int.from_bytes({_bus_var(binding.bus)}.readfrom_mem("
        f"{binding.address}, {register}, {length}), 'big')",
    ]
    if signed:
        lines += [
            f"    if raw >= {1 << (8 * length - 1)}:",
            f"        raw -= {1 << (8 * length)}",
        ]
    lines.append(f"    return raw * {scale}")
    return "\n".join(lines) + "\n"


def _mp_current_ina219(binding) -> str:
    var = _identifier(binding.name)
    shunt_ohms = binding.params.get("shunt_ohms", 0.1)
    return (
        f"# --- sensor {binding.name!r}: INA219 current sensor (shunt "
        f"voltage register, LSB 10uV) ---\n"
        f"def read_{var}():\n"
        f"    raw = int.from_bytes({_bus_var(binding.bus)}.readfrom_mem("
        f"{binding.address}, 0x01, 2), 'big')\n"
        f"    if raw >= 32768:\n"
        f"        raw -= 65536\n"
        f"    return raw * 1e-05 / {shunt_ohms}\n"
    )


def _mp_adc_mcp3008(binding) -> str:
    var = _identifier(binding.name)
    channel = binding.params.get("channel", 0)
    vref = binding.params.get("vref", 3.3)
    return (
        f"# --- sensor {binding.name!r}: MCP3008 ADC channel {channel} over SPI ---\n"
        f"_{var}_cs = Pin({binding.pins['cs']}, Pin.OUT, value=1)\n"
        f"def read_{var}():\n"
        f"    buf = bytearray(3)\n"
        f"    _{var}_cs.value(0)\n"
        f"    {_bus_var(binding.bus)}.write_readinto(bytes([1, {(8 + channel) << 4}, 0]), buf)\n"
        f"    _{var}_cs.value(1)\n"
        f"    return (((buf[1] & 3) << 8) | buf[2]) / 1023 * {vref}\n"
    )


