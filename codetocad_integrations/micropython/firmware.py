def _bus_var(bus) -> str:
    return f"_{bus.kind}{bus.id}"


def _identifier(name: str) -> str:
    return re.sub(r"\W", "_", name)


def generate_main_py(mcu: Microcontroller) -> str:
    """The agent script for ``mcu``: MicroPython ``main.py`` for
    MicroPython boards, a gpiozero-based CPython script for Python boards."""
    if mcu.board.runtime is MicrocontrollerRuntime.MICROPYTHON:
        return _generate_micropython(mcu)
    return _generate_python(mcu)


