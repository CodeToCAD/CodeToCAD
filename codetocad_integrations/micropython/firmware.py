def _mp_encoder(binding) -> str:
    var = _identifier(binding.name)
    cpr = getattr(binding.device, "counts_per_revolution", 2048)
    cpr = binding.params.get("counts_per_revolution", cpr)
    return (
        f"# --- sensor {binding.name!r}: quadrature encoder ---\n"
        f"_{var}_state = [0, 0, time.ticks_ms()]  # count, last count, last t\n"
        f"_{var}_b = Pin({binding.pins['b']}, Pin.IN)\n"
        f"def _{var}_irq(pin):\n"
        f"    _{var}_state[0] += 1 if _{var}_b.value() else -1\n"
        f"Pin({binding.pins['a']}, Pin.IN).irq(\n"
        f"    trigger=Pin.IRQ_RISING, handler=_{var}_irq)\n"
        f"def read_{var}():\n"
        f"    count = _{var}_state[0]\n"
        f"    now = time.ticks_ms()\n"
        f"    dt = time.ticks_diff(now, _{var}_state[2]) / 1000\n"
        f"    rpm = 0.0\n"
        f"    if dt > 0:\n"
        f"        rpm = (count - _{var}_state[1]) / {cpr} / dt * 60\n"
        f"    _{var}_state[1] = count\n"
        f"    _{var}_state[2] = now\n"
        f"    return {{'count': count, 'rpm': rpm}}\n"
    )


