# -- upload --


def upload(mcu: Microcontroller, port: str | None = None) -> str:
    """Generate ``main.py`` for ``mcu`` and copy it onto the board with
    mpremote, then reset so it starts running. Returns the port used."""
    if mcu.board.runtime is not MicrocontrollerRuntime.MICROPYTHON:
        raise ValueError(
            f"{mcu.board.name} is not a MicroPython board; use "
            "mcu.upload(firmware_path=...) and run the script with python3"
        )
    if port is None and isinstance(mcu.communication, SerialCommunication):
        port = mcu.communication.port
    if port is None:
        raise ValueError(
            "No serial port: pass port= or set a SerialCommunication on the "
            "microcontroller"
        )
    mpremote = shutil.which("mpremote")
    if mpremote is None:
        raise RuntimeError(
            "mpremote was not found; install it with: uv sync --extra micropython"
        )
    firmware = generate_main_py(mcu)
    with tempfile.TemporaryDirectory() as tmp_dir:
        main_py = Path(tmp_dir) / "main.py"
        main_py.write_text(firmware)
        subprocess.run(
            [mpremote, "connect", port, "fs", "cp", str(main_py), ":main.py"],
            check=True,
        )
    subprocess.run([mpremote, "connect", port, "reset"], check=True)
    return port
