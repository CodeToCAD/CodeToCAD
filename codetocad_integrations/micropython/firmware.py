def _dc_motor_dispatch(var: str, max_rpm) -> str:
    return (
        f"def command_{var}(value):\n"
        f"    if not isinstance(value, dict):\n"
        f"        value = {{'duty': float(value)}}\n"
        f"    if value.get('stop'):\n"
        f"        _{var}_duty(0.0)\n"
        f"    elif 'duty' in value:\n"
        f"        _{var}_duty(float(value['duty']))\n"
        f"    elif 'velocity_rpm' in value:\n"
        f"        _{var}_duty(float(value['velocity_rpm']) / {max_rpm})\n"
    )


def _dc_motor_drv8830(binding) -> str:
    """DRV8830 I2C motor driver (works verbatim on both runtimes; the
    Python variant monkey-patches writeto_mem onto smbus2)."""
    var = _identifier(binding.name)
    max_rpm = binding.params.get(
        "max_rpm", getattr(binding.device, "no_load_speed_rpm", None) or 300
    )
    bus = _bus_var(binding.bus)
    return (
        f"# --- actuator {binding.name!r}: DRV8830 I2C motor driver ---\n"
        f"def _{var}_duty(duty):\n"
        f"    duty = min(1.0, max(-1.0, duty))\n"
        f"    if abs(duty) < 0.02:\n"
        f"        {bus}.writeto_mem({binding.address}, 0x00, b'\\x03')  # brake\n"
        f"    else:\n"
        f"        vset = max(0x06, int(abs(duty) * 0x3F))\n"
        f"        bits = 1 if duty >= 0 else 2\n"
        f"        {bus}.writeto_mem({binding.address}, 0x00, bytes([(vset << 2) | bits]))\n"
        + _dc_motor_dispatch(var, max_rpm)
    )


def _vesc_uart(binding) -> str:
    """VESC over the microcontroller's UART. Shared by both runtimes:
    the bus variable exposes ``write()`` (machine.UART and pyserial both
    do)."""
    var = _identifier(binding.name)
    pole_pairs = binding.params.get(
        "pole_pairs", getattr(binding.device, "pole_pairs", None) or 1
    )
    bus = _bus_var(binding.bus)
    return (
        f"# --- actuator {binding.name!r}: VESC over UART "
        f"(COMM_SET_DUTY=5, COMM_SET_CURRENT=6, COMM_SET_RPM=8) ---\n"
        f"def command_{var}(value):\n"
        f"    if not isinstance(value, dict):\n"
        f"        value = {{'duty': float(value)}}\n"
        f"    if value.get('stop'):\n"
        f"        {bus}.write(_vesc_command(6, 0))\n"
        f"    elif 'duty' in value:\n"
        f"        {bus}.write(_vesc_command(5, float(value['duty']) * 100000))\n"
        f"    elif 'velocity_rpm' in value:\n"
        f"        {bus}.write(_vesc_command(8, float(value['velocity_rpm']) * {pole_pairs}))\n"
        f"    elif 'current_amps' in value:\n"
        f"        {bus}.write(_vesc_command(6, float(value['current_amps']) * 1000))\n"
    )


