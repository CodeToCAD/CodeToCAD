def _py_dc_motor(binding) -> str:
    var = _identifier(binding.name)
    max_rpm = binding.params.get(
        "max_rpm", getattr(binding.device, "no_load_speed_rpm", None) or 300
    )
    freq = binding.params.get("frequency_hz", 1000)
    return (
        f"# --- actuator {binding.name!r}: dc_motor (H-bridge PWM+DIR) ---\n"
        f"_{var}_pwm = PWMOutputDevice({binding.pins['pwm']}, frequency={freq})\n"
        f"_{var}_dir = DigitalOutputDevice({binding.pins['dir']})\n"
        f"def _{var}_duty(duty):\n"
        f"    _{var}_dir.value = 1 if duty >= 0 else 0\n"
        f"    _{var}_pwm.value = min(1.0, abs(duty))\n"
        f"def command_{var}(value):\n"
        f"    if not isinstance(value, dict):\n"
        f"        value = {{'duty': float(value)}}\n"
        f"    if value.get('stop'):\n"
        f"        _{var}_duty(0.0)\n"
        f"    elif 'duty' in value:\n"
        f"        _{var}_duty(float(value['duty']))\n"
        f"    elif 'velocity_rpm' in value:\n"
        f"        _{var}_duty(float(value['velocity_rpm']) / {max_rpm})\n"
    )


def _py_stepper(binding) -> str:
    var = _identifier(binding.name)
    step_delay = binding.params.get("step_delay_us", 800) / 1e6
    return (
        f"# --- actuator {binding.name!r}: stepper (STEP/DIR) ---\n"
        f"_{var}_step = DigitalOutputDevice({binding.pins['step']})\n"
        f"_{var}_dir = DigitalOutputDevice({binding.pins['dir']})\n"
        f"_{var}_position = [0]\n"
        f"def _{var}_move(steps):\n"
        f"    _{var}_dir.value = 1 if steps >= 0 else 0\n"
        f"    for _ in range(abs(int(steps))):\n"
        f"        _{var}_step.on()\n"
        f"        time.sleep({step_delay})\n"
        f"        _{var}_step.off()\n"
        f"        time.sleep({step_delay})\n"
        f"    _{var}_position[0] += int(steps)\n"
        f"def command_{var}(value):\n"
        f"    if not isinstance(value, dict):\n"
        f"        value = {{'steps': int(value)}}\n"
        f"    if 'steps' in value:\n"
        f"        _{var}_move(value['steps'])\n"
        f"    elif 'position_steps' in value:\n"
        f"        _{var}_move(int(value['position_steps']) - _{var}_position[0])\n"
    )


def _py_adc_mcp3008(binding) -> str:
    var = _identifier(binding.name)
    channel = binding.params.get("channel", 0)
    vref = binding.params.get("vref", 3.3)
    # On the Pi the chip select must be a hardware CE pin: GPIO8 = CE0,
    # GPIO7 = CE1 (gpiozero drives the SPI device, not a soft CS).
    device = 1 if binding.pins.get("cs") == 7 else 0
    return (
        f"# --- sensor {binding.name!r}: MCP3008 ADC channel {channel} "
        f"(hardware SPI CE{device}) ---\n"
        f"_{var}_adc = MCP3008(channel={channel}, device={device})\n"
        f"def read_{var}():\n"
        f"    return _{var}_adc.value * {vref}\n"
    )


