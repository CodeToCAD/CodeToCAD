def _py_encoder(binding) -> str:
    var = _identifier(binding.name)
    cpr = getattr(binding.device, "counts_per_revolution", 2048)
    cpr = binding.params.get("counts_per_revolution", cpr)
    return (
        f"# --- sensor {binding.name!r}: quadrature encoder ---\n"
        f"_{var}_encoder = RotaryEncoder({binding.pins['a']}, {binding.pins['b']}, max_steps=0)\n"
        f"_{var}_state = [0, time.monotonic()]\n"
        f"def read_{var}():\n"
        f"    count = _{var}_encoder.steps\n"
        f"    now = time.monotonic()\n"
        f"    dt = now - _{var}_state[1]\n"
        f"    rpm = (count - _{var}_state[0]) / {cpr} / dt * 60 if dt > 0 else 0.0\n"
        f"    _{var}_state[0] = count\n"
        f"    _{var}_state[1] = now\n"
        f"    return {{'count': count, 'rpm': rpm}}\n"
    )


